const fruits = ["Strawberry", "Apple", "Orange", "Grapes", "Cherry", "Mosambi"];

// Accessing array values
console.log(fruits[0]); // Strawberry
console.log(fruits[1]); // Apple
console.log(fruits[2]); // Orange
console.log(fruits[3]); // Grapes
console.log(fruits[4]); // Cherry
console.log(fruits[5]); // Mosambi

// Changing the values of the array
// Arrays are mutable - means the values in the array can be changed
fruits[0] = "Sitafal";
console.log(fruits[0]);
